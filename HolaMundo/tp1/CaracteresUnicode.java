public class CaracteresUnicode {
    public static void main(String[] args) {
        // Guardar los caracteres de un nombre en formato Unicode
        char c1 = '\u004A'; // J
        char c2 = '\u006F'; // o
        char c3 = '\u0070'; // p
        char c4 = '\u0061'; // a
        char c5 = '\u0073'; // s

        // Imprimir los caracteres por pantalla
        System.out.println("Los caracteres de mi nombre en Unicode son:");
        System.out.println(c1);
        System.out.println(c2);
        System.out.println(c3);
        System.out.println(c4);
        System.out.println(c5);
    }
}