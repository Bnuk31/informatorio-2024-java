public class Descontador {
    public static void main(String[] args) {
        // Declarar e inicializar el número
        int numero = 25; // Reemplaza 25 con el número que desees

        // Decrementar el número 20 veces usando un bucle
        for (int i = 0; i < 20; i++) {
            numero--; // Decrementa el número en 1
        }

        // Mostrar el resultado
        System.out.println("El número después de decrementar 20 veces es: " + numero);
    }
}
