public class Contador {
    public static void main(String[] args) {
        // Declarar e inicializar el número
        int numero = 5; // Reemplaza 5 con el número que desees

        // Incrementar el número 20 veces usando un bucle
        for (int i = 0; i < 20; i++) {
            numero++;
        }

        // Mostrar el resultado
        System.out.println("El número después de incrementar 20 veces es: " + numero);
    }
}
    