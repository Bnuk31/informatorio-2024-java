public class CalculoAreaCirculo {
    public static void main(String[] args) {
        // Definir el radio
        double radio = 5.0;

        // Calcular el área
        double area = Math.PI * Math.pow(radio, 2);

        // Mostrar el resultado
        System.out.println("El área del círculo con radio " + radio + " es: " + area);
    }
}